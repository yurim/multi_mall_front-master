        <span class="s1"
              style="font-kerning: none;">제<b>15</b>조<b> (</b>개인위치정보의<b> </b>이용<b> </b>또는<b> </b>제공<b>)</b></span></p>
      <p class="p2"
         style="margin-bottom: 0; font-variant-numeric: normal; font-variant-east-asian: normal; font-stretch: normal; font-size: 12px; line-height: normal;">
        <span class="s1" style="font-kerning: none;">회사는 개인위치정보를 이용하여 서비스를 제공하고자 하는 경우에는 미리 다음 각호의 내용을 이용약관에 명시한 후 “개인위치정보주체”의 동의를 얻어야 한다.</span>
      </p>
      <p class="p2"
         style="margin-bottom: 0; font-variant-numeric: normal; font-variant-east-asian: normal; font-stretch: normal; font-size: 12px; line-height: normal;">
        <span class="s1" style="font-kerning: none;">1. 회사는 상호, 주소, 전화번호 그 밖의 연락처를 이용자가 쉽게 알 수 있도록 서비스화면에 게시하며 약관의 내용은 이용자가 연결화면을 통하여 볼 수 있도록 한다.</span>
      </p>
      <p class="p2"
         style="margin-bottom: 0; font-variant-numeric: normal; font-variant-east-asian: normal; font-stretch: normal; font-size: 12px; line-height: normal;">
        <span class="s1" style="font-kerning: none;">2. “이용자” 및 법정대리인의 권리와 그 행사방법은 제소 당시의 이용자의 주소에 의하며, 주소가 없는 경우에는 거소를 관할하는 지방법원의 전속관할로 한다. 다만, 제소 당시 이용자의 주소 또는 거소가 분명하지 않거나 외국 거주자의 경우에는 민사소송법상의 관할법원에 제기한다.</span>
      </p>
      <p class="p2"
         style="margin-bottom: 0; font-variant-numeric: normal; font-variant-east-asian: normal; font-stretch: normal; font-size: 12px; line-height: normal;">
        <span class="s1" style="font-kerning: none;">3. 회사는 민원처리 등을 위한 위치정보 이용,제공 사실 확인자료를 자동 기록,보존하며, 해당 자료는 6개월간 보관한다. 이용자가 개인위치정보의 이용또는 제공에 대한 동의를 전부 또는 일부 철회한 경우에는 법률에 따라 보전해야 하는 경우를 제외하고 지체없이 위치정보 이용.제공과 관련한 데이터(동의의 일부 철회의 경우에는 철회와 관련된 부분)를 파기한다.</span>
      </p>
      <p class="p2"
         style="margin-bottom: 0; font-variant-numeric: normal; font-variant-east-asian: normal; font-stretch: normal; font-size: 12px; line-height: normal;">
