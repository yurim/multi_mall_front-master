        <span class="s1" style="font-kerning: none;">이용요금</span></p>
      <p class="p2"
         style="margin-bottom: 0; font-variant-numeric: normal; font-variant-east-asian: normal; font-stretch: normal; font-size: 12px; line-height: normal;">
        <span class="s1" style="font-kerning: none;"><br></span></p>
      <p class="p2"
         style="margin-bottom: 0; font-variant-numeric: normal; font-variant-east-asian: normal; font-stretch: normal; font-size: 12px; line-height: normal;">
        <span class="s1" style="font-kerning: none;">제<b>14</b>조<b> (</b>서비스<b> </b>이용요금<b>)</b></span></p>
      <p class="p2"
         style="margin-bottom: 0; font-variant-numeric: normal; font-variant-east-asian: normal; font-stretch: normal; font-size: 12px; line-height: normal;">
        <span class="s1" style="font-kerning: none;">서비스의 이용요금은 이용자가 회사의 서비스를 이용하기 위한 통신 비용이며, 이용자가 이동통신사업자에 지불하는 통신요금 외에 이용요금은 청구하지 아니한다.</span>
      </p>
      <p class="p2"
         style="margin-bottom: 0; font-variant-numeric: normal; font-variant-east-asian: normal; font-stretch: normal; font-size: 12px; line-height: normal;">
        <span class="s1" style="font-kerning: none;">단, 통신 요금은 위치정보단말의 쌍방향 통신을 위해 이동통신망을 이용함으로써 발생하는 요금으로, 통신 요금은 고객의 데이터 사용 예상치, 이동통신사 등에 따라 변동될 수 있으며 기본적으로 이동통신사가 제공하는 데이터 요금제 및 가입비 기준에 준한다.</span>
